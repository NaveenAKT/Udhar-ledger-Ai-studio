export type LanguageType = 'te' | 'en';

export interface LanguageContextValue {
  language: LanguageType;
  t: typeof TeluguTranslations;
  setLanguage: (lang: LanguageType) => void;
  toggleLanguage: () => void;
}

const LanguageContext = React.createContext<LanguageContextValue | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageType>(() => {
    const saved = localStorage.getItem('udhar_ledger_lang');
    return (saved as LanguageType) === 'te' ? 'te' : 'en';
  });

  const setLanguage = (lang: LanguageType) => {
    setLanguageState(lang);
    localStorage.setItem('udhar_ledger_lang', lang);
  };

  const toggleLanguage = () => {
    setLanguage(language === 'te' ? 'en' : 'te');
  };

  const t = language === 'en' ? EnglishTranslations : TeluguTranslations;

  return React.createElement(LanguageContext.Provider, { value: { language, t, setLanguage, toggleLanguage } }, children);
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
